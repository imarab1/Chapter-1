public class Spikey {
	public static void main (String[] args) 
	{
		System.out.println("  \\/");//Prints two slashes, one forward and one back
		System.out.println(" \\\\//");//Prints four slashes, two forward and two back
		System.out.println("\\\\\\///");//Prints six slashes, three forward and three back
		System.out.println("///\\\\\\");//Prints six slashes, three forward and three back
		System.out.println(" //\\\\");//Prints four slashes, two forward and two back
		System.out.println("  /\\");//Prints two slashes, one forward and one back
	}
}
  
  
  
